/// Data structure for the main map markers (List/Map View).
#[derive(Clone, Debug)]
pub struct MapMarkerData {
    pub id: String,
    pub latitude: f64,
    pub longitude: f64,
    pub main_goal_label: String,      // e.g., "Kino • Kawa"
    pub organizer_label: String,      // e.g., "Anna + 4"
    pub time_label: String,           // e.g., "18:00"
    pub marker_color: String,         // "#000000" or "#888888"
}

/// Data structure for the Static Mini-Map in Meeting Details.
/// Contains bounds to fit both user and meeting location.
#[derive(Clone, Debug)]
pub struct MiniMapData {
    pub center_lat: f64,
    pub center_lon: f64,
    pub zoom_level: u8,               // Calculated to fit both points
    pub user_lat: f64,
    pub user_lon: f64,
    pub meeting_lat: f64,
    pub meeting_lon: f64,
    // In a real implementation, this might be a pre-rendered image URL or binary blob
    // generated by a native renderer or a headless browser.
    pub static_image_url: Option<String>, 
}

/// Generates markers for the main map view.
pub async fn generate_map_markers(
    db_manager: Arc<RwLock<crate::db::manager::DbManager>>,
    current_timestamp: u64,
    max_distance_km: f64,
    user_lat: f64,
    user_lon: f64,
) -> Result<Vec<MapMarkerData>, &'static str> {
    let db = db_manager.read().await;
    let database = db.database();

    // Fetch active meetings. 
    // Note: Filtering by distance should ideally happen in DB query for performance,
    // but we filter here for demonstration if DB doesn't support geo-queries directly yet.
    let meetings = database
        .meetings()
        .filter(|m| m.status.eq(MeetingStatus::Active))
        .collect()
        .await
        .map_err(|_| "ERR_DB_FETCH_FAILED")?;

    let mut markers = Vec::new();

    for meeting in meetings {
        // 1. Distance Check
        let dist = haversine_distance(user_lat, user_lon, meeting.latitude, meeting.longitude);
        if dist > max_distance_km {
            continue;
        }

        // 2. Parse Tags & Filter for POSITIVE ONLY
        let cts_tags = crate::dict::cts_parser::parse_cts(&meeting.tags_cts)
            .unwrap_or_default();

        let positive_tags: Vec<&CtsTag> = cts_tags
            .iter()
            .filter(|t| t.status == TagStatus::Positive)
            .collect();

        let main_goal_label = positive_tags
            .iter()
            .map(|t| t.word.clone())
            .collect::<Vec<String>>()
            .join(" • ");

        if main_goal_label.is_empty() {
            continue; 
        }

        // 3. Organizer + Count Label
        let participant_count = database
            .meeting_participants()
            .filter(|p| p.meeting_id.eq(&meeting.id))
            .count()
            .await
            .unwrap_or(0);
        
        let organizer_name = database
            .users()
            .filter(|u| u.id.eq(&meeting.organizer_id))
            .select(|u| u.display_name.clone())
            .into_first()
            .await
            .unwrap_or_else(|| "Anon".to_string());

        let organizer_label = format!("{} + {}", organizer_name, participant_count);

        // 4. Time Label
        let start_time_dt = DateTime::from_timestamp(meeting.start_time as i64, 0)
            .unwrap_or_default()
            .with_timezone(&Local);
        let time_label = start_time_dt.format("%H:%M").to_string();

        // 5. Color
        let marker_color = if meeting.start_time as u64 >= current_timestamp {
            "#000000".to_string()
        } else {
            "#888888".to_string()
        };

        markers.push(MapMarkerData {
            id: meeting.id,
            latitude: meeting.latitude,
            longitude: meeting.longitude,
            main_goal_label,
            organizer_label,
            time_label,
            marker_color,
        });
    }

    Ok(markers)
}

/// Generates data for the Static Mini-Map in the Details View.
/// Calculates center and zoom to fit both User Location and Meeting Location.
pub async fn generate_mini_map_data(
    db_manager: Arc<RwLock<crate::db::manager::DbManager>>,
    meeting_id: &str,
    user_lat: f64,
    user_lon: f64,
) -> Result<MiniMapData, &'static str> {
    let db = db_manager.read().await;
    let database = db.database();

    let meeting = database
        .meetings()
        .filter(|m| m.id.eq(meeting_id))
        .into_first()
        .await
        .ok_or("ERR_MEETING_NOT_FOUND")?;

    let m_lat = meeting.latitude;
    let m_lon = meeting.longitude;

    // Calculate bounding box
    let min_lat = user_lat.min(m_lat);
    let max_lat = user_lat.max(m_lat);
    let min_lon = user_lon.min(m_lon);
    let max_lon = user_lon.max(m_lon);

    // Center point
    let center_lat = (min_lat + max_lat) / 2.0;
    let center_lon = (min_lon + max_lon) / 2.0;

    // Simple Zoom Calculation (Logic depends on map provider, here is a heuristic)
    // Distance between points determines zoom.
    let dist = haversine_distance(user_lat, user_lon, m_lat, m_lon);
    
    // Heuristic: closer points = higher zoom (larger number), far points = lower zoom.
    // Assuming Zoom 15 is street level (~1km view), Zoom 10 is city level.
    let zoom_level = if dist < 0.1 {
        16 // Very close, show street
    } else if dist < 1.0 {
        15
    } else if dist < 5.0 {
        13
    } else if dist < 20.0 {
        11
    } else {
        9 // Far away, show region
    };

    Ok(MiniMapData {
        center_lat,
        center_lon,
        zoom_level,
        user_lat,
        user_lon,
        meeting_lat: m_lat,
        meeting_lon: m_lon,
        static_image_url: None, // To be populated by native renderer or tile fetcher
    })
}

