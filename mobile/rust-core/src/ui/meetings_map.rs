/// Generates a URI to open the system's native map application.
/// Supports Android (geo:), iOS (maps:), and Desktop (https).
pub fn open_system_navigation(lat: f64, lon: f64, query_name: Option<&str>) -> String {
    let q = query_name.unwrap_or("Spotka Meeting");
    let encoded_q = urlencoding::encode(q);
    
    // Universal approach: Try geo URI first, fallback to HTTPS
    // Android handles geo:lat,lon?q=lat,lon(label)
    // iOS handles maps:lat,lon?q=lat,lon(label)
    
    // Using HTTPS to Google Maps / OSM is often most reliable across platforms if specific schemes fail
    // For pure native apps, we usually pass coordinates to FFI which calls Intent/URLSession
    
    // Return a generic Geo URI string that the FFI layer can parse and route correctly
    format!("geo:{},{},q({})", lat, lon, encoded_q)
}

/// Helper: Haversine distance (km)
fn haversine_distance(lat1: f64, lon1: f64, lat2: f64, lon2: f64) -> f64 {
    const R: f64 = 6371.0; // Earth radius in km
    let d_lat = (lat2 - lat1).to_radians();
    let d_lon = (lon2 - lon1).to_radians();
    let a = (d_lat / 2.0).sin().powi(2)
        + lat1.to_radians().cos() * lat2.to_radians().cos() * (d_lon / 2.0).sin().powi(2);
    let c = 2.0 * a.sqrt().atan2((1.0 - a).sqrt());
    R * c
}

